public class TipeDataCharacter {

    public static void main(String[] args) {

        //Tipe Data Character
        //Data character (huruf) di Java dipresentasikan oleh tipe char.
        //Untuk membuat data char di Java, kita bisa menggunakan tanda ' (petik satu)
        //di awal dan di akhir karakter.

        char e = 'E';
        char k = 'K';
        char o = 'O';

        System.out.println(e);
        System.out.println(k);
        System.out.println(o);

    }

}
