public class OperasiPerbandingan {
    public static void main(String[] args) {
        //Operasi Perbandingan

        /*
        Operasi perbandingan adalah operasi untuk membandingkan dua buah data.
        Operasi perbandingan adalah operasi yang menghasilkan nilai boolean (benar atau salah).
        Jika hasil operasinya adalah benar, maka nilainya adalah true.
        Jika hasil operasinya adalah salah, maka nilainya adalah false.
        */

        /*
        Operator Perbandingan
        > = Lebih Besar.
        < = Lebih Kecil.
        >= = sama dengan atau lebih besar.
        <= = sama dengan atau lebih kecil.
        == = sama dengan.
        != = tidak sama dengan.
        */

        int a = 100;
        int b = 100;

        System.out.println(a > b);//false, karena nilai a tidak lebih besar dari nilai b.
        System.out.println(a < b);//false, karena nilai b tidak lebih kecil dari nilai a.
        System.out.println(a >= b);//true, karena nilai a sama dengan dari nilai b.
        System.out.println(a <= b);//true, karena nilai a sama dengan dari nilai b.
        System.out.println(a == b);//true, karena nilai a sama dengan dari nilai b.
        System.out.println(a != b);//false, karena nilai a sama dengan dari nilai b.

    }

}
