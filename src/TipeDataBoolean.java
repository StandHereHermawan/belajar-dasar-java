public class TipeDataBoolean {

    public static void main(String[] args) {

        // Tipe Data Boolean
        // Tipe data boolean adalah tipe data yang memiliki 2 nilai, yaitu benar dan salah.
        // Tipe data boolean di Java dipresentasikan dengan kata kunci boolean.
        // Nilai benar dipresentasikan dengan kata kunci true
        // nilai salah dipresentasikan dengan kata kunci false
        // default value untuk boolean adalah false

        boolean benar = true;
        boolean salah = false;

        System.out.println(benar);
        System.out.println(salah);

    }

}
